# -*- coding: utf-8 -*-
"""
Спільні утиліти чистки тексту для всього пайплайну.
Використовуються у parse_source.py / build_rolls.py / build_sets.py,
щоб логіка чистки (емодзі, гомогліфи, зайві пробіли) була в одному
місці, а не дублювалась/розходилась по скриптах.

ВАЖЛИВО: ці функції призначені для ТЕКСТУ, що піде користувачу на екран
(назви, склад). Для роботи з реальними іменами файлів на диску (пошук
картинки для перейменування) `clean_text` застосовувати НЕ треба --
файл на диску може мати "дивний" символ у назві, і саме цей символ
потрібен, щоб його знайти.
"""
import re
import unicodedata

# --- Гомогліфи ------------------------------------------------------------
# Типова "непомітна" помилка: під час копіювання тексту з інших джерел в
# назву/склад підмішується кирилична літера, яка виглядає ідентично до
# латинської/польської (наприклад "tobikо" останню "o" -- кирилична, або
# "MAXІ" -- кирилична "І"). Мапимо лише кириличні кодові точки на латинські
# відповідники, тож жодна справжня польська літера зачеплена не буде.
_CONFUSABLES = {
    'а': 'a', 'А': 'A',
    'е': 'e', 'Е': 'E',
    'о': 'o', 'О': 'O',
    'р': 'p', 'Р': 'P',
    'с': 'c', 'С': 'C',
    'у': 'y', 'У': 'Y',
    'х': 'x', 'Х': 'X',
    'і': 'i', 'І': 'I',
    'ѕ': 's', 'Ѕ': 'S',
    'ј': 'j', 'Ј': 'J',
    'к': 'k', 'К': 'K',
    'м': 'm', 'М': 'M',
    'т': 't', 'Т': 'T',
}


def fix_confusables(s: str) -> str:
    """Заміняє кириличні "двійники" латинських літер на латинські."""
    return ''.join(_CONFUSABLES.get(ch, ch) for ch in s)


# --- Емодзі та сміття від зламаних емодзі-послідовностей ------------------
# 'So' (Symbol, other) покриває практично всі емодзі (включно з модифікато-
# рами тону шкіри). 'Cf' (Format) -- невидимі символи типу ZWJ (використо-
# вується у складених емодзі на кшталт "👨‍🍳"); коли така послідовність
# ламається під час копіювання/експорту, часто лишається сирітський "?".
_DROP_CATEGORIES = {'So', 'Cf', 'Cn'}


def strip_emoji(s: str) -> str:
    return ''.join(ch for ch in s if unicodedata.category(ch) not in _DROP_CATEGORIES)


# ізольований "?" (з пробілами/краєм рядка з обох боків) -- слід зламаної
# емодзі-послідовності, а не справжній знак питання (у назвах меню їх немає)
_LONE_QMARK_RE = re.compile(r'(?:(?<=\s)|^)\?(?=\s|$)')
_WS_RE = re.compile(r'\s+')


def clean_inline(s: str) -> str:
    """Чистка фрагмента тексту без обрізання країв (для токенів усередині
    більшого рядка, напр. окремого інгредієнта)."""
    if not s:
        return ''
    s = fix_confusables(s)
    s = strip_emoji(s)
    s = _LONE_QMARK_RE.sub('', s)
    s = _WS_RE.sub(' ', s)
    return s.strip()


def clean_text(s: str) -> str:
    """Повна чистка: гомогліфи, емодзі, зайві пробіли, зайва пунктуація
    по краях (використовувати для назв рядків/страв/сетів)."""
    s = clean_inline(s)
    return s.strip(' \t-,:;.').strip()


KNOWN_IMAGE_EXTS = ('.webp', '.jpg', '.jpeg', '.png')


def split_ext(filename: str):
    """Розділяє ім'я файлу на (базу, розширення), розпізнаючи лише відомі
    розширення картинок; якщо розширення невідоме -- вважаємо, що його
    просто немає і за замовчуванням буде .webp там, де це треба."""
    low = filename.lower()
    for ext in KNOWN_IMAGE_EXTS:
        if low.endswith(ext):
            return filename[: -len(ext)], filename[-len(ext):]
    return filename, '.webp'


def strip_trailing_known_ext(s: str) -> str:
    """Знімає '.webp'/'.jpg'/... з кінця рядка, якщо він там є (рядки
    original_menu.txt історично закінчуються на '.webp', бо походять з
    імен файлів; текст з окремого .txt-файлу цього суфіксу вже не має)."""
    base, ext = split_ext(s)
    if s.lower().endswith(ext):
        return base
    return s
